//! A minimal hand-rolled **uppercase** stick font for drawing text as line
//! segments (used to project the gobo projector's pattern name onto the wall with
//! gizmos, the same way laser patterns are drawn).
//!
//! Each glyph is a set of strokes; each stroke is a polyline of `(x, y)` points in
//! a unit cell `x ∈ [0, ~0.85]`, `y ∈ [0, 1]` (y up). Only `A–Z`, `0–9` and `_` are
//! defined; lowercase is upcased and anything else is blank (a space advance).


/// Lay a string into line segments in text space, centred at `x = 0`, with
/// `y ∈ [0, 1]`. Returns `(start, end)` point pairs ready to map onto the wall.
pub fn layout(s: &str) -> Vec<((f32, f32), (f32, f32))> {
    let chars: Vec<char> = s.chars().collect();
    let x0 = -(chars.len() as f32) * ADVANCE / 2.0;
    let mut segs = Vec::new();
    for (i, &c) in chars.iter().enumerate() {
        let gx = x0 + i as f32 * ADVANCE;
        for stroke in glyph(c) {
            for w in stroke.windows(2) {
                segs.push(((gx + w[0].0, w[0].1), (gx + w[1].0, w[1].1)));
            }
        }
    }
    segs
}
